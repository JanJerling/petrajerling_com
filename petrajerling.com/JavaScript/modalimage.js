var modal = document.getElementById('ModalMI1');

var img = document.getElementById('MI1');
var modalImg = document.getElementById("imgMI1");

img.onclick = function(){
    modal.style.display = "block";
    modalImg.src = this.src;
};

var span = document.getElementsByClassName("close") [0] /*this number must be in ascending order for each image on the page*/;

span.onclick = function() { 
    modal.style.display = "none";
};
